// Custom SVG Icon Library - Minimalist, professional icons mimicking Lucide style
// All icons are 24x24 viewbox with 2px stroke width for consistency

export interface IconProps {
  size?: number;
  color?: string;
  className?: string;
  strokeWidth?: number;
}

const defaultProps: IconProps = {
  size: 24,
  color: 'currentColor',
  className: '',
  strokeWidth: 2
};

// Icon component wrapper
function Icon(svg: string, props: IconProps = {}): string {
  const { size, color, className, strokeWidth } = { ...defaultProps, ...props };
  return svg
    .replace(/width="24"/, `width="${size}"`)
    .replace(/height="24"/, `height="${size}"`)
    .replace(/stroke="currentColor"/, `stroke="${color}"`)
    .replace(/stroke-width="2"/, `stroke-width="${strokeWidth}"`)
    .replace(/class=""/, `class="${className}"`);
}

